public class NetworkConfigExample {
    private String ip = "1.1.1.1";
    private int port = 12345;
    private String mode = "client";

    public NetworkConfig() {
    }

    public String getIp() {
        return ip;
    }

    public int getPort() {
        return port;
    }

    public String getMode() {
        return mode;
    }
}
